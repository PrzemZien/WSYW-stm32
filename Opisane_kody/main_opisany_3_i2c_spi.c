  uint8_t WHO_AM_I = 0x0F; // Tutaj jest taka wartość, ponieważ jest taka w żyroskopie służy ona do sprawdzenia czy dane urządzenia działa
  uint8_t READ =0x01 << 7 ; // Bit 0 - zapis danych, Bit 1 - odczyt danych, i  dlatego tutaj bit 7 jest ustawiony na 1
  uint8_t WRITE = 0x00 << 7 ; // tutaj bit 7 jest ustawiony na 0
  uint8_t spi_tx_buf[2]; //Bufor wysyłany za pomocą SPI. Może byc [1] wysylamy tylko jeden bajt adres rejestru polczony z bitem read
  uint8_t spi_rx_buf[8]; // Bufor do odbierania danych za pomocą SPI.

  spi_tx_buf[0] = WHO_AM_I | READ; // tutaj określamy który rejestr zapisujemy
  HAL_GPIO_WritePin(GPIOE, GPIO_PIN_3, 0); // Tutaj uruchamiam SPI
  HAL_SPI_Transmit(&hspi1, spi_tx_buf, 1, 50); // Tutaj transmituję
